package sessionbuddy.utils;

/**
 * An enumeration of different categories of data on thesession.org
 * 
 * This is aimed at making it easier to construct single methods
 * that can retrieve multiple types of data
 * 
 * @author Colman since 2018-12-12
 */
public enum DataCategory
{
    tunes,
    recordings,
    sessions,
    events,
    discussions,
    members,
    trips,
    sets
}
